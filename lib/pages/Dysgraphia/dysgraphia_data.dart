  static List<String> getPrompts(int grade, String activityType) {
    final data = getData()[grade];
    if (data == null) return [];

    switch (activityType) {
      case 'letters':
        return (data['letters'] as List?)?.cast<String>() ?? [];
      case 'words':
        return (data['words'] as List?)?.cast<String>() ?? [];
      case 'sentences':
        return (data['sentences'] as List?)?.cast<String>() ?? [];
      default:
        return [];
    }
  }

  static List<String> getLetters(int grade) {
    return getData()[grade]?['letters']?.cast<String>() ?? [];
  }

  static List<String> getWords(int grade) {
    return getData()[grade]?['words']?.cast<String>() ?? [];
  }

  static List<String> getSentences(int grade) {
    return getData()[grade]?['sentences']?.cast<String>() ?? [];
  }

  // Get item count for UI display
  static int getItemCount(int grade, String activityType) {
    final prompts = getPrompts(grade, activityType);
    // Remove duplicates for display count (but keep duplicates for actual testing)
    return prompts.toSet().length;
  }
